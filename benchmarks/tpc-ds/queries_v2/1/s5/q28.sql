select  *
from (select avg(ss_list_price) B1_LP
            ,count(ss_list_price) B1_CNT
            ,count(distinct ss_list_price) B1_CNTD
      from store_sales
      where ss_quantity between 0 and 5
        and (ss_list_price between 35 and 35+10 
             or ss_coupon_amt between 17021 and 17021+1000
             or ss_wholesale_cost between 24 and 24+20)) B1,
     (select avg(ss_list_price) B2_LP
            ,count(ss_list_price) B2_CNT
            ,count(distinct ss_list_price) B2_CNTD
      from store_sales
      where ss_quantity between 6 and 10
        and (ss_list_price between 58 and 58+10
          or ss_coupon_amt between 7772 and 7772+1000
          or ss_wholesale_cost between 37 and 37+20)) B2,
     (select avg(ss_list_price) B3_LP
            ,count(ss_list_price) B3_CNT
            ,count(distinct ss_list_price) B3_CNTD
      from store_sales
      where ss_quantity between 11 and 15
        and (ss_list_price between 52 and 52+10
          or ss_coupon_amt between 6607 and 6607+1000
          or ss_wholesale_cost between 62 and 62+20)) B3,
     (select avg(ss_list_price) B4_LP
            ,count(ss_list_price) B4_CNT
            ,count(distinct ss_list_price) B4_CNTD
      from store_sales
      where ss_quantity between 16 and 20
        and (ss_list_price between 154 and 154+10
          or ss_coupon_amt between 17664 and 17664+1000
          or ss_wholesale_cost between 56 and 56+20)) B4,
     (select avg(ss_list_price) B5_LP
            ,count(ss_list_price) B5_CNT
            ,count(distinct ss_list_price) B5_CNTD
      from store_sales
      where ss_quantity between 21 and 25
        and (ss_list_price between 11 and 11+10
          or ss_coupon_amt between 5760 and 5760+1000
          or ss_wholesale_cost between 0 and 0+20)) B5,
     (select avg(ss_list_price) B6_LP
            ,count(ss_list_price) B6_CNT
            ,count(distinct ss_list_price) B6_CNTD
      from store_sales
      where ss_quantity between 26 and 30
        and (ss_list_price between 126 and 126+10
          or ss_coupon_amt between 3853 and 3853+1000
          or ss_wholesale_cost between 49 and 49+20)) B6
limit 100;
